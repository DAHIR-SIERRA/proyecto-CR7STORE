    private void TablaPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaPMouseClicked
        if (evt.getClickCount() == 1) {
            int fila = TablaP.rowAtPoint(evt.getPoint());
            if (fila > -1) {
                idProducto.setValue((Integer) TablaP.getValueAt(fila, 0));
                Categoria.setText((String) TablaP.getValueAt(fila, 1));
                Talla.setText(""+TablaP.getValueAt(fila, 3));
                Precio.setText(TablaP.getValueAt(fila, 2) + "");
                Cantidad.setValue("" + TablaP.getValueAt(fila, 4));
                BTNModificar.setEnabled(true);
                BTNElimina.setEnabled(true);
            }
        }

    }//GEN-LAST:event_TablaPMouseClicked

