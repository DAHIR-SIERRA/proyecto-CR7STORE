    public void llenarTabla() {
        Productos unProducto = new Productos();
        DefaultTableModel tabla = (DefaultTableModel) TablaP.getModel();
        Iterator<Productos> itProductos = unProducto.Listar();
        Object[] filaProducto = new Object[TablaP.getColumnCount()];
        tabla.setRowCount(0);
        while (itProductos.hasNext()) {
            unProducto = itProductos.next();
            filaProducto[0] = unProducto.getIdProducto();
            filaProducto[1] = unProducto.getProductoCategoria();
            filaProducto[3] = unProducto.getProductoTalla();
            filaProducto[2] = unProducto.getProductoPrecio();
            filaProducto[4] = unProducto.getProductoStock();
            tabla.addRow(filaProducto);

        }
    }

    private Productos ObtenerProducto() {

        Productos elProducto = new Productos();
        elProducto.setIdProducto((Integer) idProducto.getValue());
        elProducto.setProductoCategoria((String) Categoria.getText());
        elProducto.setProductoTalla((String) Talla.getText());
        elProducto.setProductoPrecio(Integer.parseInt(Precio.getText()));
        System.out.println(""+Cantidad.getText());
        elProducto.setProductoStock(Integer.parseInt(Cantidad.getText()));

        return elProducto;
    }
    private void llenarTablaconBusqueda(String busqueda) {
        Productos unProducto = new Productos();
        DefaultTableModel tabla = (DefaultTableModel) TablaP.getModel();
        Iterator<Productos> itProducto = unProducto.buscar(busqueda);
        Object[] filaProducto = new Object[TablaP.getColumnCount()];
        tabla.setRowCount(0);
        while (itProducto.hasNext()) {
            unProducto = itProducto.next();
            filaProducto[0] = unProducto.getIdProducto();
            filaProducto[1] = unProducto.getProductoCategoria();
            filaProducto[2] = unProducto.getProductoPrecio();
            filaProducto[3] = unProducto.getProductoTalla();
            filaProducto[4] = unProducto.getProductoStock();
            ((DefaultTableModel) TablaP.getModel()).addRow(filaProducto);
        }
    }

