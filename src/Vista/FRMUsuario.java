    public void llenarTabla() {
        Usuario unUsuario = new Usuario();
        DefaultTableModel tabla = (DefaultTableModel) TBLUsuario.getModel();
        Iterator<Usuario> itUsuario = unUsuario.Listar();
        Object[] filaUsuario = new Object[TBLUsuario.getColumnCount()];
        tabla.setRowCount(0);
        while (itUsuario.hasNext()) {
            unUsuario = itUsuario.next();
            filaUsuario[0] = unUsuario.getIdUSuario();
            filaUsuario[1] = unUsuario.getUsuarioNombre();
            filaUsuario[2] = unUsuario.getUsuarioTelefono();
            filaUsuario[3] = unUsuario.getUsuarioDocumento();
            filaUsuario[4] = unUsuario.getUsuarioCorreo();
            filaUsuario[5] = unUsuario.getUsuarioRol();
            tabla.addRow(filaUsuario);

        }
}

    private Usuario ObtenerUsuario() {

        Usuario elUsuario = new Usuario();
        elUsuario.setIdUSuario((Integer) TXTIdUsuario.getValue());
        elUsuario.setUsuarioNombre((String) TXTNombreUsuario.getText());
        elUsuario.setUsuarioTelefono((String) TXTTelefonoUsuario.getText());
        elUsuario.setUsuarioDocumento ((String) TXTDocumetoUsuario.getText());
        elUsuario.setUsuarioCorreo((String)TXTCorreoUsuario.getText());
        elUsuario.setUsuarioRol((String)TXTRolUsuario.getText());
       
        return elUsuario;
    }
    private void llenarTablaconBusqueda(String busqueda) {
        Usuario unUsuario = new Usuario();
        DefaultTableModel tabla = (DefaultTableModel) TBLUsuario.getModel();
        Iterator<Usuario> itUsuario = unUsuario.buscar(busqueda);
        Object[] filaUsuario = new Object[TBLUsuario.getColumnCount()];
        tabla.setRowCount(0);
        while (itUsuario.hasNext()) {
            unUsuario = itUsuario.next();
            filaUsuario[0] = unUsuario.getIdUSuario();
            filaUsuario[1] = unUsuario.getUsuarioNombre();
            filaUsuario[2] = unUsuario.getUsuarioTelefono();
            System.err.println("pasa por aca");
            filaUsuario[3] = unUsuario.getUsuarioCorreo();
            filaUsuario[4] = unUsuario.getUsuarioDocumento();
            filaUsuario[5] = unUsuario.getUsuarioRol();
            ((DefaultTableModel) TBLUsuario.getModel()).addRow(filaUsuario);

        }
    }
    

    

