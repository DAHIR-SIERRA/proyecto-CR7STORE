    private void TBLUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBLUsuarioMouseClicked
        if (evt.getClickCount() == 1) {
            int fila = TBLUsuario.rowAtPoint(evt.getPoint());
            if (fila > -1) {
                TXTIdUsuario.setValue((Integer) TBLUsuario.getValueAt(fila, 0));
                TXTNombreUsuario.setText((String) TBLUsuario.getValueAt(fila, 1));
                TXTTelefonoUsuario.setText((String) TBLUsuario.getValueAt(fila, 2));
                TXTCorreoUsuario.setText((String) TBLUsuario.getValueAt(fila, 4));
                TXTDocumetoUsuario.setText((String) TBLUsuario.getValueAt(fila, 3));
                TXTRolUsuario.setText((String)TBLUsuario.getValueAt(fila, 5));
                BTNModificar.setEnabled(true);
                BTNEliminar.setEnabled(true);
            }
        }
    }//GEN-LAST:event_TBLUsuarioMouseClicked

    
